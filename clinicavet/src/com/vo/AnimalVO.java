public class AnimalVO {

    private String nome_animal;
    private String idade_animal;
    private String sexo_animal;

    public String getNome_animal() {
        return nome_animal;
    }

    public void setNome_animal(String nome_animal) {
        this.nome_animal = nome_animal;
    }

    public String getIdade_animal() {
        return idade_animal;
    }

    public void setIdade_animal(String idade_animal) {
        this.idade_animal = idade_animal;
    }

    public String getSexo_animal() {
        return sexo_animal;
    }

    public void setSexo_animal(String sexo_animal) {
        this.sexo_animal = sexo_animal;
    }
}
